    private void populateByMName(String name) {
        DefaultTableModel model = (DefaultTableModel) infoTable.getModel();
        model.setRowCount(0);
        for(Network network:system.getNetworkList()) {
            for (Enterprise en: network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise.getEnterpriseType().toString().equals("CinemaEn")) {
                    for(Organization org: en.getOrganizationDirectory().getOrganizationList()) {
                        for(UserAccount ua: org.getUserAccountDirectory().getUserAccountList()) {
                            if (ua.getRole().toString().equals("CinemaManager")) {
                                for(CinemaRequest crq : ua.getCinemaQueue().getCinemaRequestList()) {
                                    if (crq.getMovieName().equals(name)) {
                                        Object[] row = new Object[5];
                                        row[0] = crq;
                                        row[1] = crq.getMovie().getType();
                                        row[2] = crq.getMovie().getDuration();
                                        row[3] = crq.getMovie().getDirector().getUsername();
                                        row[4] = crq.getCinemaAdmin().getUsername();
                                        model.addRow(row);
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }

