    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblManage.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        Car c = (Car) tblManage.getValueAt(selectedRowIndex, 0);
        
        UpdateCarJPanel ucjp = new UpdateCarJPanel(workAreaJPanel, c, admin);
        workAreaJPanel.add("UpdateCarJPanel", ucjp);
        CardLayout layout = (CardLayout) workAreaJPanel.getLayout();
        layout.next(workAreaJPanel);

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblManage.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Car c = (Car) tblManage.getValueAt(selectedRowIndex, 0);
        admin.getCarCatalog().removeCar(c);
        JOptionPane.showMessageDialog(this, "Car deleted.");
        refreshTable();        
        
    }//GEN-LAST:event_btnDeleteActionPerformed


