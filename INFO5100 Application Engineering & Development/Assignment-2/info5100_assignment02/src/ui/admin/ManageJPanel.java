    public void refreshTable() {

        DefaultTableModel model = (DefaultTableModel) tblManage.getModel();
        model.setRowCount(0);

        for (Car c : admin.getCarCatalog().getCarcatalog()) {
            Object row[] = new Object[10];
            row[0] = c;
            row[1] = c.getCarId();
            row[2] = c.getCarBrand();
            row[3] = c.getCarModel();
            row[4] = c.getCarCity();
            row[5] = c.getCarYear();
            row[6] = c.getCarSeat();
            row[7] = c.getIsAvail();
            row[8] = c.getIsExpired();
            row[9] = c.getTime();

            model.addRow(row);
        }
    }

