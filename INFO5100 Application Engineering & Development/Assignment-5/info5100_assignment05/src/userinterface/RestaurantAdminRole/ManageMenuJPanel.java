    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblMenu.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select a food", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Food f = (Food) tblMenu.getValueAt(selectedRowIndex, 0);
        resAccount.getOrderQueue().getFoodCatalog().removeProduct(f);
        JOptionPane.showMessageDialog(this, "Food Deleted");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

