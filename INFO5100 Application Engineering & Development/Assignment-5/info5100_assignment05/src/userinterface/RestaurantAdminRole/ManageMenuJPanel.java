      private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
        model.setRowCount(0);
        //System.out.println(resAccount.getOrderQueue());
        if(resAccount.getOrderQueue().getFoodCatalog()==null){
            foodCatalog=new FoodCatalog();
            resAccount.getOrderQueue().setFoodCatalog(foodCatalog);
            return;
        }
        List<Food> productcatalog = resAccount.getOrderQueue().getFoodCatalog().getProductcatalog();
        for (Food p : productcatalog) {
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getModelNumber();
            row[2] = p.getPrice();
            model.addRow(row);
        }
    }
