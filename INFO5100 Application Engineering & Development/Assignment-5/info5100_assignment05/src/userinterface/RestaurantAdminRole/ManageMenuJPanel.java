    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        double price =0.0;
        try{
            price=Double.parseDouble(txtPrice.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please check the price format", "Warning", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Food p = foodCatalog.addProduct();
        p.setProdName(txtName.getText());
        p.setPrice(price);
        JOptionPane.showMessageDialog(null, "Food added!", "Info", JOptionPane.INFORMATION_MESSAGE);
        populateTable();
        
    }//GEN-LAST:event_btnCreateActionPerformed

